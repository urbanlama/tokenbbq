// Build-time embed of the TokenBBQ brand PNG into the CLI bundle.
// Reads widget/src/assets/tokenbbq-icon.png and writes src/dashboard-icon.ts
// with the base64-encoded data URL — so the dashboard HTML can render the
// brand mark without depending on a runtime file path. Same trick as
// scripts/inline-wasm.mjs (which solves the same Bun --compile problem).
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';

const repoRoot = path.resolve(import.meta.dirname, '..');
const src = path.join(repoRoot, 'widget', 'src', 'assets', 'tokenbbq-icon.png');
const dst = path.join(repoRoot, 'src', 'dashboard-icon.ts');

const bytes = readFileSync(src);
const base64 = bytes.toString('base64');

const out = `// Auto-generated by scripts/inline-dashboard-icon.mjs — do not edit.
// Source: widget/src/assets/tokenbbq-icon.png (${bytes.length} bytes)
export const DASHBOARD_BRAND_ICON_DATA_URL =
\t'data:image/png;base64,${base64}';
`;

writeFileSync(dst, out);
console.log(`[inline-icon] ${src} → ${dst} (${bytes.length} bytes → ${base64.length} chars base64)`);
